package org.apache.hadoop.mapred.iterative;

public interface IDistanceMeasure<K, V> {

	/**
	 * get the distance between two output tuples with the same key
	 * @param key1
	 * @param value1
	 * @param key2
	 * @param value2
	 * @return
	 */
	public float getDistance(K key1, V value1, K key2, V value2);

}
